# Convertir un entero, incluyendo la base

# Base decimal
a = int('23', 10)

# Base binario
a = int('10111', 2)

# Base octal
a = int('27', 8)

# Base hexadecimal
a = int('17', 16)

# Base 5
a = int('444', 5)

print(f'a: {a}')
